     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $usuobs = Usuobs::findOrFail($id);
        $usuobs->usuario_id = $request->usuario_id;
        $usuobs->observacion_id = $request->observacion_id;
        $usuobs->update();
