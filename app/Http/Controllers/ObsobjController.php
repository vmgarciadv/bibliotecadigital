     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $obsobj = Obsobj::findOrFail($id);
        $obsobj->observacion_id = $request->observacion_id;
        $obsobj->objetivo_id = $request->objetivo_id;
        $obsobj->update();
    }

